    private void editValue() {
        List<JTextField> list = getAllText();
        switch (jFrame.currentTitle) {
            case "Clients": {
                PPE.INSTANCE.getClientManager().addClient(list.get(2).getText(), list.get(0).getText(), Integer.parseInt(list.get(1).getText()));
                break;
            }
            case "Commandes": {
                PPE.INSTANCE.getCommandeManager().addCommande(new Commande(-1, list.get(0).getText(), list.get(1).getText(), Integer.parseInt(list.get(2).getText()), Integer.parseInt(list.get(3).getText())));
                break;
            }
            case "Plugins": {
                PPE.INSTANCE.getPluginManager().addPlugin(list.get(3).getText(), Float.parseFloat(list.get(0).getText()), Float.parseFloat(list.get(1).getText()), Integer.parseInt(list.get(2).getText()), 1);
                break;
            }
        }
        new Thread(() -> jFrame.update()).start();
        close();
    }

    public List<JTextField> getAllText() {
        List<JTextField> compList = new ArrayList<>();
        for (Component component : getAllComponents(this)) {
            if (component instanceof JTextField) {
                compList.add(((JTextField)component));
            }
        }
        return compList;
    }

    public List<Component> getAllComponents(final Container c) {
        Component[] comps = c.getComponents();
        List<Component> compList = new ArrayList<Component>();
        for (Component comp : comps) {
            compList.add(comp);
            if (comp instanceof Container) {
                compList.addAll(getAllComponents((Container) comp));
            }
        }
        return compList;
    }

